<h2>Distinctives</h2>

<p>
There are a number of distinctive things that make Lincoln Road Chapel what it is, in a way that is noticeable to others.
</p>

<h4>Diversity</h4>
<p>
Lincoln seeks to recognize, respect and minister to both Christ followers and those who haven't yet established a personal relationship with Christ, regardless of race, age, education, language, economics or status.
</p>

<h4>Dual Culture</h4>
<p>
Lincoln has been blessed by God with a congregation that worships and serves in English and Mandarin, who work together to honour and share the good news of Jesus Christ.
</p>

<h4>Communion-Centered</h4>
<p>
Lincoln is unusual in that it celebrates communion and remembers Jesus Christ in his death and resurrection every week, as Jesus requested and the church in Acts chapter 2 practiced.
</p>

<h4>Community Core Resource</h4>
<p>
The leadership of Lincoln shows its gratitude for the excellent facility that God has given us by sharing it generously with other churches and ministries for office space and special events, with the community as a sports and training venue, and for cultural and social support.
</p>

<h4>Ministry to the Broken</h4>
<p>
Lincoln has become, through God's grace, a place of safety and healing for people dealing with hurts, addictions and other difficulties in life's journey, through hosting a Biblical 12 step support ministry and provision of office space for counselors. For more information see <a href="programs-celebrate-recovery.php">Celebrate Recovery</a>. 
</p>

<h4>Friendliness</h4>
<p>
Many churches are friendly, as they should be. Lincoln's diversity, openness and dual culture creates a unique atmosphere that many find very welcoming.
</p>
