<h1>Welcome!</h1>

<p>
  As a newcomer, it can be quite overwhelming to begin attending a new church!  You will likely have questions about how to belong, how to get to know people, and what the process is for getting involved in serving.
</p>

<p>
  We host newcomer lunches at different times throughout the year, with the purpose of getting to know you more personally and to determine what might be most helpful for you at this time of your life.  When you attend a welcome lunch, you will discover some great options for personal growth: small groups for those who are searching out what they believe, groups for new believers to learn the foundational truths of Jesus Christ, and opportunities for ongoing discipleship.
</p>

<p>
  <a href="/programs-celebrate-recovery.php">Celebrate Recovery</a>, a 12-step program,  is designed to help with growth through the painful circumstances that come our way, as well as finding healing from life's hurts, habits and hang-ups. When it comes to serving, there is the option of signing up to take our SHAPE course.  Here you will discover what your gifts are and we can help you find the places where you can use them!
</p>

<p>
  We want to encourage you towards the kind of personal growth on your faith journey that is life changing! It is our prayer that Lincoln Road Chapel will be a place where you can join with others in real-life discipleship.
</p>

<p>
  So please give us your information, either through email or by filling out one of the communication cards that are passed around on Sunday mornings. This will help us to connect with you and make sure you don't miss the invitation to lunch.  You can also call the chapel to speak directly to one of our pastors at any time.
</p>

<p>
  To contact a pastor please go to <a href="/contact.php">Staff Profiles</a>.
</p>
