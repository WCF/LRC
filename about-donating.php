<h2>Donating to Lincoln Road Chapel</h2>

<p>
We rely on dedicated believers to support our ministries. Most of the money donated comes from the members of the Lincoln Road Chapel church family who give because they see something happening here that they want to support financially. These generous and ongoing contributions help become nurture the faith of our members so that they will powerfully impact their generation to the glory of God.
</p>

<h3>Frequently Asked Questions About Donating</h3>

<h4>How can I donate to Lincoln Road Chapel?</h4>
<p>
There are two ways you can donate to Lincoln Road Chapel You can write a cheque to Lincoln Road Chapel and place it in the offering box located at the auditorium entrance. Or you can set up Automatic Monthly Withdrawal. You can do this by completing the Automatic Withdrawal Form and placing it in the offering box located at the auditorium entrance.
</p>

<h4>Why is there no offering taken up in the Sunday morning service?</h4>
<p>
There are several reasons. First, we don't want to embarrass our friends and visitors at our main service by making them feel they should be contributing to an offering. Paying our church family's expenses is for the church family to do, not for visitors and friends. We also believe that God knows what our needs are, and that He will prompt members of His church family to give to meet those needs.
</p>

<h4>How much do people give?</h4>
<p>
That's a matter between the individual and God. We do not teach tithing, an Old Testament principle based on giving 10%. We believe that the New Testament teaches that all that we have is given to us by the grace of God. Thus, it is a matter of personal conscience and good stewardship how we use what God has given to us. We also believe that giving should not be simply out of our surplus, but that we would want to give sacrificially in a way that alleviates the suffering of others and helps extend the reach of God's Kingdom.
</p>

<h4>So, if I want to give, how do I do it?</h4>
<p>
There is a wooden box attached to the wall just inside the entrance to the sanctuary. You can place a cheque or envelope there. Cheques should be made out to "Lincoln Road Chapel".
</p>

<h4>Can I get a receipt for charitable donations by giving to Lincoln Road Chapel?</h4>
<p>
Yes! Lincoln Road Chapel is registered with Revenue Canada. You may use a cheque with your name and address on it or you may place your offering in an envelope with your name and address on it. It is also possible to arrange direct bank deposit by getting a form from the Treasurer. All of your donations to Lincoln Road Chapel will be totaled, and a tax receipt for the whole year will be issued in February.
</p>

<h4>I heard someone mention "designations". What does that mean?</h4>
<p>
Some people may want to direct a gift to another ministry or to a specific need in the church. Designations need to be approved by the Finance Committee to ensure that we comply with Revenue Canada regulations. Please discuss your intent with the Treasurer in advance to ensure that the designation can be made within Revenue Canada guidelines.
</p>

<h4>Do we ever take public offerings at Lincoln?</h4>
<p>
Yes, we do. On occasion we might have a special offering for a missionary, a special project or for the Lincoln Road Chapel Family fund. Usually these special offerings are announced one or two Sundays in advance. However, we would prefer that the special offering not interfere with our regular giving and the operation our Lincoln's ministries.
</p>

<h4>Is everything I give to Lincoln Road Chapel used at Lincoln Road Chapel?</h4>
<p>
No. Lincoln Road Chapel is involved with a number of missionaries and other worthy works, so some of the money given here is forwarded to places where it can make a difference and do a lot of good. For example, a number of people who attended Lincoln Road when they were students are now working as missionaries abroad. It is our privilege to partner with them and to encourage them financially.
</p>

<h4>Can I give to things other than Lincoln Road Chapel?</h4>
<p>
Of course! All of your giving is voluntary, between you and God. We encourage people to pray about what God would have them give - how much and to what. We do hope that you will see that Lincoln Road Chapel is being used by God and that it deserves your support.
</p>

<h4>How do I know that the money I'm giving is being used wisely?</h4>
<p>
The Leadership Team at Lincoln Road Chapel believes that we need to be accountable and transparent in our financial dealings. We review finances at congregational meetings, invite input into our budget development and will provide financial statements to members of our church family who want to review them. Please feel free to bring questions about the finances at Lincoln Road Chapel to the Treasurer or to any member of the Leadership Team. The budgeting process begins in the Fall with the Treasurer, Leadership Team, and Operations Council and includes presentation of a proposed budget to the November congregational meeting. That proposal is refined on the basis of feedback and additional inputs and presented to the congregation at the first congregational meeting in the new year. Anyone who identifies Lincoln Road Chapel as their home church is encouraged to attend those congregational meetings.
</p>
