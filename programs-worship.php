        <h3>Worship Planning Team (WPT)</h3>

        The WPT is comprised of three to four people from both congregations and
        also a member of the Leadership Team. Tiffany Svensson-Knight is currently
        serving as WPT Coordinator and the other team members currently include:
        Heidi Mikitka, and David Knight from the Leadership Team. The Worship
        Leaders and Speakers for the Sundays being planned are also invited to
        these planning meetings. Every two weeks, the team meets to plan two to
        three services and also evaluate the success of the previous two services.
        We consider a service to be a success if one or more of the following
        happens:
        <ul>
            <li>God is glorified and worshiped</li>
            <li>The work of Jesus Christ is proclaimed</li>
            <li>People feel renewed in their spirit and inspired to follow Jesus
                more unreservedly
            </li>
            <li>People look forward to coming back</li>
        </ul>
