    <h3>Curriculum</h3>
    <p>Our new curriculum, 252 Basics, is action-packed and involves a high-energy large group worship and teaching time
        called Power UP! as well as intentional small group shepherding.</p>
    <p>The lessons are built around three Basic Truths every child should embrace according to what Jesus modeled in
        Luke 2:52.</p>
    <ul>
        <li>I need to make the wise choice.</li>
        <li>I can trust God no matter what.</li>
        <li>I should treat others the way I want to be treated.</li>
    </ul>
    <p>Every week children are reminded of these Basic Truths and learn a new key point derived from the Bible lesson.
        Every month we focus on a different Christ-like virtue and we work on a memory verse for the entire month that is
        related to that months virtue.</p>

    <h3>Volunteers</h3>
    <p>Each grade level is shepherded by a volunteer from our congregation.  Through teaching, praying, befriending and
        giving, the shepherd encourages and challenges the child to link with God and the people around them.  Shepherd also
        build close relationships with the child and their family.  All volunteers have completed police checks and are
        trained in our child abuse policy</p>

    <h3>Bringing It Home</h3>
    <p>Each week children receive fridge door sheets that provide you with information about what your child is learning,
        announcements about upcoming events, and discussion-creating questions.  Please use these to connect with your child
        and help them apply their learning to their lives.</p>
    <p>Remember, as a parent you have the most spiritual influence on your child.</p>
    <p>God-Time Cards are also sent home every week with your child.  These contain 4 devotions related to what they learned
        on Sunday.  Read and talk through these devotions a day at a time.  There are only 4, so you get 3 days off!</p>

