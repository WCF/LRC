<h2>Governance</h2>

<p>
This is just another word for "How do things get decided and done around here?"
</p>
<p>
Lincoln Road Chapel is governed by a Leadership Team of elders who are appointed by the existing elders after congregational affirmation. Affirmation of existing elders occurs every two years. We expect elders to measure up to the qualities listed in 1 Timothy 3 and Titus 1. Currently half of the Leadership Team are paid pastoral staff and half are not. We have a constitution and are incorporated federally. All members of the Leadership Team are members of the corporation. Members of the Leadership Team who are not financially remunerated by the church are Directors of the corporation.
</p>
<p>
The Leadership Team meets once a month to:
<ul>
<li>Establish direction and goals for the congregation</li>
<li>Approve and review policies and position papers</li>
<li>Hire and hold paid staff accountable</li>
<li>Approve budgets for the church</li>
<li>Approve financial support and accountability for missionaries</li>
<li>Monitor delivery of and report on pastoral care</li>
<li>Pray for the ministries and people of the church and contacts outside the church</li>
<li>Share information, insights, experiences which will help the church move ahead</li>
<li>Identify, develop, train and empower others to achieve the goals</li>
</ul>
</p>
<p>
The Leadership Team has established the Senior Management Team (SMT) consisting of the three senior staff (Lead Pastor, the Executive Pastor and the Pastor for Spiritual Development) to handle day-to-day operations and decisions on their behalf. 
</p>
<p>
The Leadership Team also appoints ministry leaders who meet at least once a year with the Leadership Team to report progress, describe their plans and suggest support needed for the coming period and to pray together. Each ministry leader has a staff member or Leadership Team member with whom they are in frequent contact for support and accountability.
</p>
<p>
Congregational input is crucial to the unity of and commitment to the church. It occurs in a number of ways: through ministry leaders and their ministry partners, periodic focus groups discussing major directional issues, and an annual Congregational Meeting usually held in February or March to affirm the annual budget and ministry direction. 
</p>
<p>
Our leadership principle is "Groups guide and govern, individuals lead."
</p>  
