<p>The Lincoln Road Mandarin Chinese Congregation is a specialized entity of Lincoln Road Chapel. It seeks to meet the spiritual needs and development of primarily the mainland Chinese community in the Kitchener-Waterloo region. We desire to see people introduced to a life changing relationship with Jesus through friendships with God's people and to see people flourish spiritually through Biblical input in their heart language and in culturally sensitive ways. We want to raise up generations of people who love God and their fellow-man passionately and wholeheartedly. There are a variety of opportunities available, including Mandarin services every Sunday morning, small Home Groups, life-on-life nurturing and outreaches.</p>

<blockquote>
  "I thank God for leading us to Lincoln Road Chapel and its Mandarin Fellowship. Although we have been believers for years in China, it has been through the encouragement and personal instruction from the fellowship that we have matured spiritually, especially in the areas of prayer, applying God's Word to our daily lives, and the confidence that we can help others spiritually as well." ~Zhigang Zhao and Shufeng Ding
</blockquote>

<h3>Mandarin Service</h3>
<div>
  When: Each Sunday at 11:00am<br>
  Where: Lincoln Road Chapel, room 235
</div>
<p>The purpose of this service is to provide corporate worship and teaching to inspire and instruct people to become fully committed and fruitful followers of Jesus Christ. The service includes worship and life-directed teaching of God's Word and is geared to both seekers investigating Christianity and confirmed followers of Jesus.</p>

<h3>Community Home Groups</h3>
<p>Our Community Home Groups provide an informal and friendly opportunity to learn about a life-changing relationship with Jesus Christ, build friendships, find mutual support and reach out to those who need God. Typically a Home Group consists for six to fifteen people and meets two to four times a month.</p>

<h3>Life-On-Life Nurturing</h3>
<p>Life-on-life nurturing is an opportunity for new believers to be personally mentored by someone who has known Jesus for a longer period of time.  We believe it takes more than mere words to meet the spiritual needs of people.  We desire to share our lives and personally champion the development and fruitfulness of new believers.  Authentic ministry is incarnational.  It is life on life.</p>

<p>Much of our outreach is under the banner of Chinese-Canadian Friendships, and include fun activities like trips to Niagara Falls and the Fall Colours trip in October, Cherry picking, Barbeques, Chinese New Year's celebrations and helpful English Conversational classes.</p>
