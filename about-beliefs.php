<div class="beside-toc">
    <h1>Beliefs</h1>

    <h4>The Bible</h4>
    <p>
    The Bible is inspired of God, inerrant in the original documents and of final authority in all matters of faith and practice.
    </p>

    <h4>God</h4>
    <p>
    There is one God, eternal, existing in three persons - Father, Son and Holy Spirit.
    </p>

    <h4>Jesus Christ</h4>
    <p>
    The Lord Jesus Christ is fully God, born of a virgin and sinlessly perfect. His sacrifice is substitutionary and representative. He rose bodily from the dead and ascended to His Father's right hand, where He now ministers as the believer's Great High Priest.
    </p>

    <h4>Salvation</h4>
    <p>Each member of the human race is sinful and re-birth by the Holy Spirit is absolutely essential for the salvation of man. Redemption is wholly by the blood of Christ and salvation is by grace, through faith in the Lord Jesus Christ.
    </p>

    <h4>Eternal Life</h4>
    <p>
    Every believer possesses eternal life and is justified, sanctified and indwelt by the Holy Spirit, and by Him is empowered to live a Godly life. The believer is secure for all eternity. However a believer can, through sin, lose his fellowship, joy, power, testimony and reward and incur the Father's chastisement. Relationship is eternal, being established by new birth; fellowship however, is dependent upon obedience.
    </p>

    <h4>Judgment</h4>
    <p>
    There will be a resurrection of the believer unto eternal life and of the unbeliever unto eternal punishment.
    </p>

    <h4>The Church</h4>
    <p>
    The church began with the descent of the Holy Spirit at Pentecost and is composed of all believers in the Lord Jesus Christ. The local church is composed of believers in a locality who gather in Christ's name for worship, prayer, edification and witness. Government and discipline are divinely outlined in the Epistles as being the responsibility of the local church.  The Lord Jesus Christ is the giver of spiritual gifts for ministry in the church, such as evangelists, pastors, teachers, etc. These gifts are given that believers may be equipped for work in His service and that the church may be built up.
    </p>

    <h4>The Ordinances</h4>
    <p>
    There are two Christian ordinances - Baptism and the Lord's supper. Baptism signifies that the believer, having died with Christ, is buried with Him and is also risen with Christ to walk in newness of life. The Lord's Supper was instituted by the Lord Himself. In the observance of this supper, believers retell the message of the Lord's death, until His return.
    </p>

    <h4>The Return of Christ</h4>
    <p>
    The personal return of the Lord Jesus Christ could occur at any moment. His return will usher in a series of events; the rapture, judgment on unredeemed society, the restoration of Israel to Him as Messiah and an eternal state of blessedness for the redeemed. This truth is an ongoing incentive to holy living and faithful service for every believer.
    </p>
</div>
